    private void tabel_transaksi_pulsaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_transaksi_pulsaMouseClicked
        // TODO add your handling code here:
         int baris = tabel_transaksi_pulsa.rowAtPoint(evt.getPoint());
        String id_kustomer = tabel_transaksi_pulsa.getValueAt(baris, 1).toString();
        txt_id_kustomer.setText(id_kustomer);
        
        String kustomer = tabel_transaksi_pulsa.getValueAt(baris, 2).toString();
        txt_nama_kustomer.setText(kustomer);
        
        String jenis_kartu = tabel_transaksi_pulsa.getValueAt(baris, 3).toString();
        cmb_operator.setSelectedItem(jenis_kartu);
        
        String tanggal = tabel_transaksi_pulsa.getValueAt(baris, 4).toString();
        Datetime.setText(tanggal);
        
        String jumlah = tabel_transaksi_pulsa.getValueAt(baris, 5).toString();
        txt__jumlah_pembelian.setText(jumlah);
        
        String pembayaran = tabel_transaksi_pulsa.getValueAt(baris, 6).toString();
        txt_pembayaran.setText(pembayaran);
        
        String kembalian = tabel_transaksi_pulsa.getValueAt(baris, 7).toString();
        lbl_kembalian.setText(kembalian);
    }//GEN-LAST:event_tabel_transaksi_pulsaMouseClicked

