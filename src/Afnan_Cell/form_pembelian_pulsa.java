     private void tampilkan_data(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No. ");
        model.addColumn("ID Kustomer ");
        model.addColumn("Nama Kustomer ");
        model.addColumn("Operator ");
        model.addColumn("Tanggal Pembelian ");
        model.addColumn("Jumlah Pembelian ");
        model.addColumn("Pembayaran ");
        model.addColumn("Kembalian ");
    
        try{
            String cari = txt_cari.getText();
            int no = 1;
            String sql = "SELECT * FROM transaksi_pulsa WHERE tgl_beli LIKE'%"+cari+"%'";
            java.sql.Connection conn = (Connection)Konfig1.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),res.getString(7)});
            }
            tabel_transaksi_pulsa.setModel(model);
        }catch (SQLException e){
            System.out.println("Error : " + e.getMessage());
        }
   }

