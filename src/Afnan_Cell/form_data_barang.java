    private void klikActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_klikActionPerformed
        // TODO add your handling code here:
        Calendar kal = new GregorianCalendar();
        int tahun = kal.get(Calendar.YEAR);
        int bulan = kal.get(Calendar.MONTH)+1;
        int tanggal = kal.get(Calendar.DAY_OF_MONTH);
        String TANGGAL = tanggal+"-"+bulan+"-"+tahun;
        DateTimes.setText(TANGGAL);
    }//GEN-LAST:event_klikActionPerformed

    private void tabel_barangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_barangMouseClicked
        // TODO add your handling code here:
        int baris = tabel_barang.rowAtPoint(evt.getPoint());
        String id_barang = tabel_barang.getValueAt(baris, 1).toString();
        txt_id_barang.setText(id_barang);

        String kode_barang = tabel_barang.getValueAt(baris, 2).toString();
        txt_kode_barang.setText(kode_barang);

        String nama_barang = tabel_barang.getValueAt(baris, 3).toString();
        cmb_nama_barang.setSelectedItem(nama_barang);

        String operator = tabel_barang.getValueAt(baris, 4).toString();
        cmb_operator.setSelectedItem(operator);

        String hrg_barang = tabel_barang.getValueAt(baris, 6).toString();
        txt_harga_barang.setText(hrg_barang);

    }//GEN-LAST:event_tabel_barangMouseClicked


